package io.github.kuri_megane;

public class GamaData {

    private String name;
    private int score;

    public GamaData(){
    }

    public void setName(String name){
        this.name = name;
    }

    public void setScore(int score){
        this.score = score;
    }

    public String getName(){
        return name;
    }

    public int getScore(){
        return score;
    }

}
